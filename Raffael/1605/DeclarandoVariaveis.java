public class DeclarandoVariaveis {

    public static void main(String[] args) {

        var textstring = "Olá";
        var numberint = 10;
        var numberdouble = 10.3;
        var booleanastrue = true;
        var booleanasfalse = false;
        // var (variable) é utilizado para definir o nome de referência para aquele espaço de memória.
        // Acima é possível ver exemplos do "var", com os tipos de variáveis.

        System.out.println(textstring);
        System.out.println(numberint);
        System.out.println(numberdouble);
        System.out.println(booleanastrue);
        System.out.println(booleanasfalse);

        // As funções podem armazenar vários tipos de variáveis/dados, sendo eles :
        // ( Variáveis/Dados são espaços de memória alocados representado por tipos.  )

        //Variaveis -> tipo: Textos (Strings)
        //Variaveis -> tipo: Numeros (Int)
        //Variaveis -> tipo: Numeros fracionados (Double)
        //Variaveis -> tipo: booleanas (verdadeiro / falso)

        // TIPOS DE DADOS

                                                    //	[ STRING ] - Textos ;

        // Deve seguir essa ordem :

        //---------------------------------------------------------------------------------------------------------------
                        // (tipo) + (nome da referencia) + (=) + (recebe o conteúdo da referência)
        //---------------------------------------------------------------------------------------------------------------

        // Desse modo :

        //---------------------------------------------------------------------------------------------------------------
                                                    // String login = "Olá";
        //---------------------------------------------------------------------------------------------------------------

        // Nos módulos antigos do java, é necessário declarar o tipo de dado antes de definir o mesmo.


	                                        // [ NUMEROS (INT) ] - Números inteiros ;

        // Deve seguir essa ordem :

        //---------------------------------------------------------------------------------------------------------------
                        // (tipo) + (nome da referencia) + (=) + (recebe o conteúdo da referência)
        //---------------------------------------------------------------------------------------------------------------

        // Desse modo :

        //---------------------------------------------------------------------------------------------------------------
                                                        // int number1 = 10;
        //---------------------------------------------------------------------------------------------------------------


	                            // [ NUMEROS FRACIONADOS (DOUBLE) ] - Números fracionados ;

        // Deve seguir essa ordem :

        //---------------------------------------------------------------------------------------------------------------
                        // (tipo) + (nome da referencia) + (=) + (recebe o conteúdo da referência)
        //---------------------------------------------------------------------------------------------------------------

        // Desse modo :

        //---------------------------------------------------------------------------------------------------------------
                                                    // double percent = 10,5;
        //---------------------------------------------------------------------------------------------------------------


	                        //[ EXPRESSÃO BOOLEANA (TRUE/FALSE) ] - Estado verdadeiro/falso ;

        // Deve seguir essa ordem :

        //---------------------------------------------------------------------------------------------------------------
                            // (tipo) + (nome da referencia) + (=) + (recebe o conteúdo da referência)
        //---------------------------------------------------------------------------------------------------------------

        // Desse modo :

        //---------------------------------------------------------------------------------------------------------------
                                                // boolean verdadeira =  true;
                                                // boolean falsa =  false;
        //---------------------------------------------------------------------------------------------------------------

        //DECLARANDO VARIAVEL


        String saudacao = "Bom dia";
        System.out.println(saudacao);

        int number;
        number = 55;
        System.out.println(number + 10);

        double numberdoublee;
        numberdoublee = 10.50;
        System.out.println(numberdoublee);

        //boolean verdadeira = true;
    }
}
